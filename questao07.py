'''
Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.
'''

print("Me informe 3 notas escolares suas que calcularei sua média: ")
nota1 = float(input("informe sua nota: "))
nota2 = float(input("informe sua nota: "))
nota3 = float(input("informe sua nota: "))

media = (nota1 + nota2 + nota3)/3

print("A média de suas notas é: ",media)